"""
Operadores de comparação:
 == Igual a
 != Diferente de
 > Maior que
 < Menor que
 >= Maior ou igual a
 <= Menor ou igual a
"""

# Igual a ==

print(10 == 10)
print(10 == 10.0)
print(10 == "10")
print(10 == 250 / 50 * 2)
print("senha123" == "Senha123")
print()

# Diferente !=

print(10 != 10)
print(10 != 10.0)
print(10 != "10")
print(10 != 250 / 50 * 2)
print("senha123" != "Senha123")
print()

# Maior que >

print(10 > 2)
print(10 > 50)
print(10 > 1000/900)
print(len("senha123*") > 8)
print()

# Menor que <

print(10 < 2)
print(10 < 50)
print(10 < 1000/900)
print(len("senha123*") < 8)
print()

# Maior ou igual a >=

print(10 >= 2)
print(10 >= 10)
print(10 >= 50)
print(len("senha123") >= 8)
print()

# Menor ou igual a <=

print(10 <= 2)
print(10 <= 10)
print(10 <= 50)
print(len("senha123") <= 8)

